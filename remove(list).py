# remove()
a = ["apple", "banana", "cherry"]
a.remove("banana")
print(a)

# pop()
a = ["apple", "banana", "cherry"]
a.pop()   # remove last item
print(a)
a.pop(0)  # remove first item
print(a)

# del
a = ["apple", "banana", "cherry"]
del a[0]
print(a)

# clear()
a = ["apple", "banana", "cherry"]
a.clear()
print(a)

# VIBGYOR
a = (input("Enter the letter:"))
if a == "V" or a == "v":
    print("Violet")
elif a == "I" or a == "i":
    print("Indigo")
elif a == "B" or a == "b":
    print("Blue")
elif a == "G" or a == "g":
    print("Green")
elif a == "Y" or a == "y":
    print("Yellow")
elif a == "O" or a == "o":
    print("Orange")
elif a == "R" or a == "r":
    print("Red")
else:
    print("Invalid")

# AND operator (Returns True if both statements are true)
x = 5
y = 3
print(x > 5 and y < 10)

# OR operator (Returns True if one of the statements is true)
a = 5
b = 3
print(a > 5 or b < 10)

# NOT operator (Reverse the result, returns False if the result is true)
